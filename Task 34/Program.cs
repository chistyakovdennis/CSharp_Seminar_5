// Задача 34: Задайте массив заполненный случайными положительными трёхзначными числами. Напишите программу, которая покажет количество чётных чисел в массиве.
// [345, 897, 568, 234] -> 2

class Program {
    static void Main(string[] args) {
        int[] array = new int[10]; // создание массива из 10 элементов

        Random random = new Random(); // создание объекта класса Random для генерации случайных чисел

        int count = 0; // счётчик чётных чисел

        // заполнение массива случайными положительными трёхзначными числами
        for (int i = 0; i < array.Length; i++) {
            array[i] = random.Next(100, 1000);
        }

        // подсчёт количества чётных чисел в массиве
        for (int i = 0; i < array.Length; i++) {
            if (array[i] % 2 == 0) {
                count++;
            }
        }

        Console.WriteLine("Количество чётных чисел в массиве: " + count);
    }
}
